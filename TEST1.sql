SELECT MAKHOA,SONGAYNV AS 'TIEN' FROM BENHNHAN INNER JOIN KHOAKHAM ON KHOAKHAM.MAKHOA = BENHNHAN.MAKHOA 
GROUP BY MAKHOA having TIEN = SONGAYNV*80000